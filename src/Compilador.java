    private void colorAnalysis() {
        /* Limpiar el arreglo de colores */
        textsColor.clear();
        /* Extraer rangos de colores */
        LexerColor lexerColor;
        try {
            File codigo = new File("color.encrypter");
            FileOutputStream output = new FileOutputStream(codigo);
            byte[] bytesText = jtpCode.getText().getBytes();
            output.write(bytesText);
            BufferedReader entrada = new BufferedReader(new InputStreamReader(new FileInputStream(codigo), "UTF8"));
            lexerColor = new LexerColor(entrada);
            while (true) {
                TextColor textColor = lexerColor.yylex();
                if (textColor == null) {
                    break;
                }
                textsColor.add(textColor);
            }
        } catch (FileNotFoundException ex) {
            System.out.println("El archivo no pudo ser encontrado... " + ex.getMessage());
        } catch (IOException ex) {
            System.out.println("Error al escribir en el archivo... " + ex.getMessage());
        }
        Functions.colorTextPane(textsColor, jtpCode, new Color(40, 40, 40));
    }

    int i = 0;

    private void fillTableTokens() {
        tokens.forEach(token -> {
            Object[] data = new Object[] { token.getLexicalComp(), token.getLexeme(),
                    "[" + token.getLine() + ", " + token.getColumn() + "]" };
            Functions.addRowDataInTable(tblTokens, data);
        });
        tokens.forEach(token -> {

            if (token.getLexicalComp().equals("Identificador")) {

                Object[] data = new Object[] { token.getLexicalComp(), token.getLexeme(),
                        tokens.get(i - 1).getLexeme() };
                Functions.addRowDataInTable(tblTokens2, data);
            }
            i++;
        });
        i = 0;
    }

    private void printConsole() {
        int sizeErrors = errors.size();
        if (sizeErrors > 0) {
            Functions.sortErrorsByLineAndColumn(errors);
            String strErrors = "\n";
            for (ErrorLSSL error : errors) {
                String strError = String.valueOf(error);
                strErrors += strError + "\n";
            }
            jtaOutputConsole
                    .setText("Compilación terminada...\n" + strErrors + "\nLa compilación terminó con errores...");
        } else {
            // Mostrar mensajes de error para los tokens de tipo ERROR
            for (Token token : tokens) {
                if (token.getLexicalComp().equals("ERROR")) {
                    String errorMessage = "Error léxico: Token no reconocido - Lexema: " + token.getLexeme()
                            + " - Línea: " + token.getLine() + " - Columna: " + token.getColumn();
                    jtaOutputConsole.append(errorMessage + "\n");
                }
            }

            jtaOutputConsole.append("Compilación terminada...");
        }
        jtaOutputConsole.setCaretPosition(0);
    }

    private void clearFields() {
        Functions.clearDataInTable(tblTokens);
        Functions.clearDataInTable(tblTokens2);
        jtaOutputConsole.setText("");
        tokens.clear();
        errors.clear();
        identProd.clear();
        identificadores.clear();
        codeHasBeenCompiled = false;
    }

